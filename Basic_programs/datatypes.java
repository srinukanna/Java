package Basic_programs;

public class datatypes {
    public static void main(String[] args)
    {
        byte var1=10;
        short var2=20;
        int var3=3000;
        long var4=3000000000L;

        System.out.println("byte value:" +var1);
        System.out.println("short value:"+var2);
        System.out.println("int value:" +var3);
        System.out.println("long value:"+var4);
    }
    
}
